public class SumOperation {
    public static void main(String args[]){
        int a=15;
        int b=25;
        System.out.println("sum= "+(a+b));
        System.out.println("sub= "+(a-b));
        System.out.println("mul= "+(a*b));
        System.out.println("div= "+(a/b));
        System.out.println("float= "+(a*1.0/b));
   }
}